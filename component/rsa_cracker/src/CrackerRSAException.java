public class CrackerRSAException extends Exception {
    public CrackerRSAException() {
        super();
    }

    public CrackerRSAException(String message) {
        super(message);
    }

    public CrackerRSAException(String message, Throwable cause) {
        super(message, cause);
    }

    public CrackerRSAException(Throwable cause) {
        super(cause);
    }
}
